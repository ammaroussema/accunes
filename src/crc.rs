pub fn apply_crc_override(crc_val: u32, mapper: &mut u16, has_chr_rom: &mut bool) {
    if let Some(entry) = lookup_crc_override(crc_val) {
        if entry.mapper >= 0 {
            let emapper = entry.mapper as u32;
            let mask = if emapper & 0x1000 != 0 { 0xFFF } else { 0xFF };
            let new_mapper = (emapper & mask) as u16;
            if *mapper != new_mapper {
                *mapper = new_mapper;
            }
            if emapper & 0x800 != 0 {
                *has_chr_rom = false;
            }
        }
    }
}

pub fn apply_crc_mirror_override(crc_val: u32, mirror_h: &mut bool, mirror_4sc: &mut bool) {
    if let Some(entry) = lookup_crc_override(crc_val) {
        if entry.mirror >= 0 {
            match entry.mirror {
                0 => { *mirror_h = true; *mirror_4sc = false; }
                1 => { *mirror_h = false; *mirror_4sc = false; }
                2 | 8 => { *mirror_4sc = true; }
                _ => {}
            }
        }
    }
}
